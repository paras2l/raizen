  private register(plugin: RaizenPlugin) {
    this.plugins.set(plugin.id, plugin);
  }

  getPlugin(id: string): RaizenPlugin | undefined {
    return this.plugins.get(id);
  }

  get(id: string): RaizenPlugin | undefined {
    return this.getPlugin(id);
  }

  getAllPlugins(): RaizenPlugin[] {
    return Array.from(this.plugins.values());
  }

  getAll(): RaizenPlugin[] {
    return this.getAllPlugins();
  }

  async executeAction(pluginId: string, actionId: string, params: Record<string, any>) {
    const plugin = this.getPlugin(pluginId);
    if (!plugin) return { success: false, error: `Plugin ${pluginId} not found.` };
    return await plugin.execute(actionId, params);
  }

  async genesisSync(): Promise<void> {
    console.log('[REGISTRY] Triggering Global Genesis Synchronization...');
    const core = this.getPlugin('core.singularity');
    if (core) {
      await core.execute('synchronize', { isInternal: true });
    }
  }

  async initializeAll(): Promise<void> {
    for (const plugin of this.plugins.values()) {
      try {
        await plugin.initialize();
      } catch (error) {
        console.error(`Failed to initialize plugin ${plugin.id}:`, error);
      }
    }
    // Perform the Genesis Handshake once all plugins are online
    await this.genesisSync();
  }
